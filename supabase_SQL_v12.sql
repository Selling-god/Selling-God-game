판매의 신 v11 - 거래 아이템 500종 완전판

- 25개 분류마다 20종씩, 정확히 500개의 서로 다른 거래 아이템을 추가했습니다.
- 각 아이템은 이름, 분류, 평균가격, 등급, 등장 확률, 표시 아이콘이 각각 지정됩니다.
- 경매장, 전당포, 중고장터, 내 가방이 같은 500종 DB를 사용합니다.
- 기존 보유 아이템과 거래 기록은 삭제하지 않습니다.

적용
1. index.html, style.css, game.js, assets 폴더를 교체합니다.
2. Supabase SQL Editor에서 supabase_SQL_v11.sql을 실행합니다.
3. 마지막 total_trade_items 값이 500 이상인지 확인합니다.
4. item_catalog_500.csv에서 전체 목록을 확인할 수 있습니다.
